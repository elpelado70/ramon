y atractiva: estepas planas, montañas y desiertos se
reparten en más de medio millón de kilómetros cuadrados. La
mayor cantidad de reservas naturales del mundo se concentra
en su inmenso territorio: 20 millones de hectáreas han
sido declaradas áreas protegidas, lo que equivale a un 14%
